public class Celsius {

    public static void main(String[] args) {
        double fahrenheit = 52.0;
        double celsius;
        celsius = 5.0 / 9.0 * (fahrenheit - 32);
        System.out.println("Fahrenheit:" + fahrenheit);
        System.out.println("Celsius:" + celsius);
    }
    
}
